class VariableExample {
	static main(args) {
		// static typing
		// so format of static typing is <data type>/<reference or variablename>

		int firstNumber = 5

		println firstNumber

//		firstNumber = "Hello" // error
//
//		println firstNumber

		println ""

		def variable = 5
		println variable

		variable = "Hello"
		println variable
	}
}